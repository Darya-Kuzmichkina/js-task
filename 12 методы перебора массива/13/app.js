//На входе n – количество элементов массива, затем пользователь заполняет массив с клавиатуры.
//Создать новый массив, где каждый элемент возведен в квадрат. Использовать метод map.
let n = prompt();
let arr = [];

for (let i = 0; i < n; i++) {
    let num = prompt(`Введите элемент ${i + 1}:`);
    

    // Проверка на ввод числа
    if (!isNaN(num)) {
        arr.push(num);
    } else {
        console.error("Введите корректное число.");
        i--; // Повторить ввод для этого элемента
    }
}

// Создание нового массива с использованием map
const squaredArr = arr.map(function(el) {
    return el * el; // Возведение в квадрат
});

// Вывод результата
console.log("Исходный массив:", arr);
console.log("Новый массив (возведенный в квадрат):", squaredArr);